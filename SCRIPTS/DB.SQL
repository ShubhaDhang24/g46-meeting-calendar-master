Create SCHEMA IF  NOT EXISTS meeting_calender_db;
USE meeting_calender_db;
create table USER(USERNAME varchar(255) NOT NULL PRIMARY KEY,
PASSWORD VARCHAR(255)  NOT NULL,
EXPIRED TINYINT DEFAULT FALSE,
CREATE_DATE DATETIME default now()
);
create TABLE MEETING_CALENDER(
 ID INT NOT NULL auto_increment PRIMARY KEY,
 USERNAME VARCHAR(255) NOT NULL,
 TITLE VARCHAR(255),
 CREATEDATE DATETIME default now(),
 foreign key(USERNAME)references USER (USERNAME)

);
create TABLE MEETING(
ID INT NOT NULL auto_increment PRIMARY KEY,
TITLE VARCHAR(255),
START_TIME DATETIME NOT NULL,
END_TIME datetime NOT NULL,
_DESCRIPTION TEXT,
CALENDER_ID INT NOT NULL,
CREATEDATE DATETIME default now(),
foreign key(CALENDER_ID) references MEETING_CALENDER(ID)


);